    "  \x1b[31m\u2298\x1b[0m CANCELLED    Workflow cancelled",
    "",
    "\x1b[1mStep Tabs\x1b[0m",
    "",
    "  1: Overview    Step summary + workflow counts",
    "  2: Timeline    Human-readable agent/LLM event summary",
    "  3: Raw         Raw stdout/stderr/progress lines",
    "  4: Diffs       File patches and diffs",
    "  5: Usage       Token/cost/prompt usage summary",
    "  6: Result      Final WorkerResult details",
    "  7: Core        Core stderr logs (supervised)",
    "  8: Agents      Agent messages & tasks (all agents)",
