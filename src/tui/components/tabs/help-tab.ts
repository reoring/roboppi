    "  0: Help        This help screen",
    "",
    "\x1b[1mAgent Tabs\x1b[0m  (shown when an agent is selected)",
    "",
    "  1: Chat        Chat messages + inline input (i)",
    "  2: Agent       Agent status, uptime, recent activity",
    "  3: Timeline    Human-readable agent/LLM event summary",
